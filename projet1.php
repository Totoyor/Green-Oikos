										Green Oikos a conscience que les pays arides souffrent d'un manque croissant d’eau potable. Dans ces pays aux régions désertiques, l’une des seules façons de récupérer de l’eau est de trouver des concepts astucieux et écologiques, ici récolter de l'eau issue de la rosée. Ce procédé a des avantages et des inconvénients. Le coût de sa mise en place et de son entretien est presque nul. Mais la production d'eau reste modeste et variable selon les conditions météorologiques. 
                    <br/>
                    <br/>
										C’est donc pour cela que Green Oikos lance le projet Ind’eau :
                    <br/>
                    <br/>
										Ce projet consiste à utiliser la rosée du matin. Ce principe repose sur le résultat de la condensation de la vapeur d’eau contenue dans l’air une fois que son humidité atteint 100% et grâce à un système de bâches qui ont le rôle de condensateur. Celles-ci sont faites en plastique recyclé et sont disposées en V afin que la rosée du matin s'écoule vers la gouttière au bout de laquelle est fixé un filtre pour que l’eau devienne potable. L'eau se déverse dans une cuve de stockage afin d'être réutilisée. 
                    <br/>
                    <br/>
										Nous allons également équiper les maisons de gouttières afin de récupérer l’eau pendant la saison des pluies. Cette eau sera stockée dans des citernes souples souterraines afin que celle-ci ne s’évapore pas. 
