						       		<p> 
                      Le projet Ecol’eau est un projet visant à rendre des bâtiments scolaires et familiaux totalement autonomes, et ce de manière écologique. Ceci est un projet de grande envergure.
                      <br/>
                      
                      <br/>
                      Pour commencer, nous allons créer ces infrastructures grâce aux produits recyclés que nous allons récupérer directement dans des décharges. Ces maisons seront faites de pneus, bouteilles en plastique, torchis, etc. Les fondations des ces maisons seront faites de bois et les éléments recyclés viendront s’ajouter au torchis afin de le consolider.
                      <br/>
                      <br/>
                      De plus, pour produire de l’électricité, des miroirs seront disposés de manière à capter et à faire converger les rayons du soleil en un point afin de faire bouillir de l’eau. Cette eau qui boue, dégagera de la vapeur que nous utiliserons pour créer de l’énergie et alimenter le bâtiment en question en électricité. Quelques panneaux solaires placés sur le toit permettront de faire l'appoint en énergie en fonction de ce que demandera le bâtiment.
                      <br/>
                      <br/>
                      En ce qui concerne l’eau, plusieurs moyens seront mis en place. Des condensateurs de rosée seront disposés non loin du bâtiment. Ce système permettra de récupérer la rosée et de la transformer en eau potable. Des puits aériens, systèmes de bâches accrochées à trois arbres convergeant en un point. Sous ce point de convergence, l’eau de la rosée tombera dans une citerne placée sous terre afin de préserver l’eau du soleil. Des gouttières capteront l’eau lors des saisons des pluies, qui sera directement acheminée dans les citernes souterraines. En plus de cela, des récupérateurs d’eau seront disposés à côté de chaque habitation afin d'accroître la récupération d’eau de pluie.
                      <br/>
                      <br/>
                      Enfin, pour permettre une agriculture intelligente, des systèmes d’irrigations plus économiques seront disposés de manière réfléchie et permettront donc une agriculture à moyenne échelle, mais sûre en terme de longévité. 

									</p>
